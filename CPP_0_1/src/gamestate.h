#ifndef GAMESTATE_H
#define GAMESTATE_H

class OpenAnno;

class GameState
{
public:
	GameState(OpenAnno* app);
	virtual ~GameState();
	virtual bool requestExit() = 0;
	virtual void update(float delay) = 0;
	// These function are called when a gamestate is pushed at top or poped from top of the gamestate stack
	virtual void activate();
	virtual void deactivate();

protected:
	OpenAnno* m_app;
};

#endif
